    public void Logar(){
        String Login, Senha, Nome, Endereco, Telefone;     

        Login = JTLogin.getText();
        Senha = JTSenha.getText();         
        Nome = JTNome.getText();
        Endereco = JTEndereco.getText();   
        Telefone = JTTelefone.getText();  

        UsuarioDTO objusuariodto = new UsuarioDTO();

        objusuariodto.setLogin(Login);
        objusuariodto.setSenha(Senha);
        objusuariodto.setNome(Nome);
        objusuariodto.setEndereco(Endereco);
        objusuariodto.setTelefone(Telefone);

        ConexaoCi objusuariodao = new ConexaoCi();
        int Resultado = objusuariodao.autenticarUsuario(objusuariodto);

        if(Resultado > 0){
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso");
        }else{
            JOptionPane.showMessageDialog(null, "Falha no cadastro");
        }
    }

    private void JBOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBOkActionPerformed
        // TODO add your handling code here:
        try{
          String nome = JTextNome.getText();
            String genero;
            double peso = Double.parseDouble(JTextPeso.getText());
        
        if(JRBM.isSelected()){
            genero = "Masculino";
            JOptionPane.showMessageDialog(rootPane, "Nome: "+ nome + "\nPeso: "+ peso + "Kg"+"\nSexo: " + genero);
            System.exit(0);
        }
        if(JRBF.isSelected()){
            genero = "Feminino";
            JOptionPane.showMessageDialog(rootPane, "Nome: "+ nome + "\nPeso: "+ peso + "Kg"+"\nSexo: " + genero);
            System.exit(0);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "ERRO: PESO INV√ÅLIDO.");
            JTextPeso.setText("");
        }
    }//GEN-LAST:event_JBOkActionPerformed


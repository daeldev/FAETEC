    private void JBTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTotalActionPerformed
        // TODO add your handling code here:
        double batata=0, Hamburguer=0, Coca=0, Cheesebacon=0, Pizza=0, total=0;
        
        if (JCBatata.isSelected()){
            batata = 20;
        }
        if (JCHamburguer.isSelected()){
            Hamburguer = 15;
        }
        if (JCCoca.isSelected()){
            Coca = 7;
        }
        if (JCCheesebacon.isSelected()){
            Cheesebacon = 13;
        }
        if (JCPizza.isSelected()){
            Pizza = 45;
        }   
        
        total = batata + Hamburguer + Coca + Cheesebacon + Pizza;
        JTextTotal.setText(Double.toString(total));
    }//GEN-LAST:event_JBTotalActionPerformed


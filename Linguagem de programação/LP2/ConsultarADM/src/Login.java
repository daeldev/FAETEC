    public void Logar(){
        try{
            String Login, Senha;     
            Login = JTLogin.getText();
            Senha = JTSenha.getText();      
            
            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setLogin(Login);
            objusuariodto.setSenha(Senha);
            
            UsuarioConexaoci objusuariodao = new UsuarioConexaoci();
            ResultSet rusuarioci = objusuariodao.autenticarUsuario(objusuariodto);
            
            if(rusuarioci.next()){
                Teste onjfrmcadastra = new Teste();
                onjfrmcadastra.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Login e senha inv√°lido");
            }
        }catch (SQLException erro){
            JOptionPane.showMessageDialog(null, "FRMLOGINVIM" + erro);
        }
    }
    
